class PlotParams:
    """Class to store plot parameters.
    """
    # Default figure size
    fig_size = (15, 10)
    # Smaller Figure Size for small Heatmaps
    small_heatmap = (10, 10)
    # Default Figure Size for bigger Heatmaps
    big_heatmap = (15, 10)
    # Font Size for text inside the plot
    font_size = 22
    # Font Size for the title
    title_font_size = 26
    # Padding for the title
    title_padding = 30
